package LogicalProgramming;

import java.util.Scanner;

public class Printnto1Number {

	public static void main(String[] args) {

		Scanner scn = new Scanner(System.in);
		System.out.println("Limit");
		int a = scn.nextInt();
		// for loop
		for (int i = a; i >= 1; i--) {
			System.out.println(i);
		} // for12

	}// main

}// class
